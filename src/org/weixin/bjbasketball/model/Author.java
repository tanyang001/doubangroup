package org.weixin.bjbasketball.model;

public class Author {

    private String name;
    
    private String is_suicide;
    
    private String avatar;
    
    private String alt;
    
    private String uid;
    
    private String id;
    
    private String type;
    
    private String large_avatar;
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIs_suicide() {
        return is_suicide;
    }

    public void setIs_suicide(String isSuicide) {
        is_suicide = isSuicide;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getAlt() {
        return alt;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getLarge_avatar() {
        return large_avatar;
    }

    public void setLarge_avatar(String largeAvatar) {
        large_avatar = largeAvatar;
    }
    
}
